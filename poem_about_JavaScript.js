// Two ways to write multiline strings:

// 1.With escape sequence
let poem = "Roses are red,\nViolets are blue, \nJavaScript is fun,\nAnd so are you."

console.log(poem)

// 2.With template literal 
let poem = `Roses are red,
Violets are blue,
JavaScript is fun,
And so are you.`

console.log(poem)